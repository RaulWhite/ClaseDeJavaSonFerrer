
public class EjemploBuclesAnidados1 {
	
	public static void main(String[] args) {
		
		// Programa que escribe 5 filas con 10 asteriscos cada una
		
		for (int i = 1; i <= 5; i++) {
			
			for (int j = 1; j <= 10; j++) {
				System.out.print("*");				
			}
			
			System.out.println();
		}
				
	}

}
